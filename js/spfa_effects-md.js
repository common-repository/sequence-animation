// Return an effect options object for the given parameters:
function _normalizeArguments( spfa_effect, options, speed, callback ) {

	// Allow passing all options as the first parameter
	if ( $.isPlainObject( spfa_effect ) ) {
		options = spfa_effect;
		spfa_effect = spfa_effect.spfa_effect;
	}

	// Convert to an object
	spfa_effect = { spfa_effect: spfa_effect };

	// Catch (effect, null, ...)
	if ( options == null ) {
		options = {};
	}

	// Catch (effect, callback)
	if ( $.isFunction( options ) ) {
		callback = options;
		speed = null;
		options = {};
	}

	// Catch (effect, speed, ?)
	if ( typeof options === "number" || $.fx.speeds[ options ] ) {
		callback = speed;
		speed = options;
		options = {};
	}

	// Catch (effect, options, callback)
	if ( $.isFunction( speed ) ) {
		callback = speed;
		speed = null;
	}

	// Add options to effect
	if ( options ) {
		$.extend( spfa_effect, options );
	}

	speed = speed || options.duration;
	spfa_effect.duration = $.fx.off ? 0 :
		typeof speed === "number" ? speed :
		speed in $.fx.speeds ? $.fx.speeds[ speed ] :
		$.fx.speeds._default;

	spfa_effect.complete = callback || options.complete;

	return spfa_effect;
}

function standardAnimationOption( option ) {

	// Valid standard speeds (nothing, number, named speed)
	if ( !option || typeof option === "number" || $.fx.speeds[ option ] ) {
		return true;
	}

	// Invalid strings - treat as "normal" speed
	if ( typeof option === "string" && !$.spfa_effects.spfa_effect[ option ] ) {
		return true;
	}

	// Complete callback
	if ( $.isFunction( option ) ) {
		return true;
	}

	// Options hash (but not naming an effect)
	if ( typeof option === "object" && !option.spfa_effect ) {
		return true;
	}

	// Didn't match any standard API
	return false;
}

